#include "hb_config.h"

#include <malloc.h>
#include <string.h>

//////////////////////////////////////////////////////////////////////////
void * hb_new( size_t _size )
{
    void * p = malloc( _size );
    memset( p, 0, _size );

    return p;
}
//////////////////////////////////////////////////////////////////////////
void hb_free( void * _ptr )
{
    free( _ptr );
}
